public class Problem03 {

	public static void main(String[] args) {

		// H 출력 
		for (char i = 65; i < 91; i++) {
				
			if (i == 'H') {
				System.out.print(i);
				break;
			}
		}

		// E 출력 
		for (char i = 65; i < 91; i++) {
				
			if (i == 'E') {
				System.out.print(i);
				break;
			}
		}

		// LL 출력 
		for (char i = 65; i < 91; i++) {
				
			if (i == 'L') {
				System.out.print(i);
				System.out.print(i);
				break;
			}
		}

		// O 출력 
		for (char i = 65; i < 91; i++) {
				
			if (i == 'O') {
				System.out.print(i);
				break;
			}
		}

	}
}